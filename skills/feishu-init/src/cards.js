export function healthReportText(checklist) {
  const line = (c) => `${c.ok ? '✅' : '❌'} ${c.item}${c.ok ? '' : ` —— ${c.hint}`}`;
  const missing = (checklist || []).filter((c) => !c.ok);
  const head = missing.length === 0 ? '配置体检：全部就位 ✅，可以开始用了。' : `配置体检：还差 ${missing.length} 项，按下面指引补齐：`;
  return [head, ...(checklist || []).map(line)].join('\n');
}
